import lombok.extern.slf4j.Slf4j;
import org.apache.http.annotation.ThreadSafe;
import org.apache.ibatis.io.Resources;
import org.apache.ibatis.session.SqlSession;
import org.apache.ibatis.session.SqlSessionFactory;
import org.apache.ibatis.session.SqlSessionFactoryBuilder;
import org.apache.ibatis.session.SqlSessionManager;

import java.io.IOException;
import java.io.InputStream;

/**
 * @Author: Near
 * @Date: 2018/11/20 16:17
 * @Description: 数据库工具类 基于mybatis
 */
@Slf4j
@ThreadSafe
public class DBUtils {

    private DBUtils(){

    }
    private final static SqlSessionFactory  instance;

    static {
        String resource = "mybatis-config.xml";
        InputStream inputStream = null;
        try {
            inputStream = Resources.getResourceAsStream(resource);
        } catch (IOException e) {
            throw new RuntimeException("init mybatis configuration file failure.",e);
        }
        instance = new SqlSessionFactoryBuilder().build(inputStream);
    }

    public static SqlSessionFactory getSqlSessionFactory(){
        return instance;
    }

    public static SqlSession getSqlSession(boolean autoCommit){
        return SqlSessionManager.newInstance(instance).openSession(autoCommit);
    }

}
